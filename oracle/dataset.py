"""
PyTorch Dataset for the SAIR IC50 oracle.

Each sample is a (protein_embedding, ligand_graph, pIC50) triple.
Protein embeddings are loaded from a precomputed ESM2 cache.
Ligand graphs are built on-the-fly from SMILES using RDKit + PyG.
"""

from __future__ import annotations

from pathlib import Path
from typing import Optional

import pandas as pd
import torch
from torch.utils.data import Dataset
from torch_geometric.data import Data

from rdkit import Chem


# ── Atom & bond featurisation ──────────────────────────────────────────────────

ATOM_SYMBOLS = ["C", "N", "O", "S", "F", "P", "Cl", "Br", "I", "other"]


def one_hot(value, choices: list) -> list[int]:
    return [int(value == c) for c in choices]


def atom_features(atom) -> list[float]:
    """14-dimensional atom feature vector."""
    symbol = atom.GetSymbol()
    return (
        one_hot(symbol if symbol in ATOM_SYMBOLS[:-1] else "other", ATOM_SYMBOLS)
        + [atom.GetFormalCharge()]
        + [atom.GetTotalNumHs()]
        + [float(atom.GetIsAromatic())]
        + [atom.GetMass() / 100.0]
    )


def bond_features(bond) -> list[float]:
    """3-dimensional bond feature vector."""
    return [
        bond.GetBondTypeAsDouble() / 3.0,
        float(bond.GetIsConjugated()),
        float(bond.IsInRing()),
    ]


def smiles_to_graph(smiles: str) -> Optional[Data]:
    """Convert a SMILES string to a PyG Data object. Returns None if invalid."""
    mol = Chem.MolFromSmiles(smiles)
    if mol is None:
        return None

    x = torch.tensor(
        [atom_features(a) for a in mol.GetAtoms()], dtype=torch.float
    )

    edge_index, edge_attr = [], []
    for bond in mol.GetBonds():
        i, j = bond.GetBeginAtomIdx(), bond.GetEndAtomIdx()
        feat = bond_features(bond)
        edge_index += [[i, j], [j, i]]
        edge_attr += [feat, feat]

    if len(edge_index) == 0:
        edge_index = [[0, 0]]
        edge_attr = [[0.0, 0.0, 0.0]]

    edge_index = torch.tensor(edge_index, dtype=torch.long).t().contiguous()
    edge_attr = torch.tensor(edge_attr, dtype=torch.float)

    return Data(x=x, edge_index=edge_index, edge_attr=edge_attr)


# ── Protein filter ─────────────────────────────────────────────────────────────

def load_keep_proteins(annotations_csv: str | Path) -> set[str]:
    """
    Load the set of proteins to keep from data/long_protein_annotations.csv.
    Generated by notebooks/01_data_exploration.ipynb cell 8.7.
    Short proteins are always kept. Long proteins are kept only if UniProt
    confirms all annotated binding sites are within the first 1022 residues.
    """
    df = pd.read_csv(Path(annotations_csv).expanduser())
    return set(df[df["keep"] == True]["protein"].tolist())


# ── Dataset ────────────────────────────────────────────────────────────────────

class SAIRDataset(Dataset):
    """
    Loads SAIR parquet, filters rows, and serves
    (protein_embedding, ligand_graph, pIC50) triples.

    Args:
        parquet_path: Path to sair.parquet.
        esm_cache_dir: Directory containing precomputed ESM2 embeddings,
            one file per unique protein: <uniprot_id>.pt  (shape: [D]).
        annotations_csv: Path to data/long_protein_annotations.csv.
            Used to filter out long proteins with at-risk binding sites.
        entry_ids: Optional list of entry_ids to restrict to (for splits).
        filter_all_passed: If True, drop rows that failed PoseBusters.
        assay_filter: Keep only rows with this assay type
            ('biochem', 'cell', 'homogenate', None for all).
        deduplicate: If True, keep one row per (sequence, SMILES) pair,
            choosing the sample with highest iptm.
    """

    def __init__(
        self,
        parquet_path: str | Path,
        esm_cache_dir: str | Path,
        annotations_csv: str | Path = "data/long_protein_annotations.csv",
        entry_ids: Optional[list] = None,
        filter_all_passed: bool = True,
        assay_filter: Optional[str] = "biochem",
        deduplicate: bool = True,
    ):
        self.esm_cache_dir = Path(esm_cache_dir).expanduser()

        # ── Load keep-protein set ─────────────────────────────────────────────
        keep_proteins = load_keep_proteins(annotations_csv)
        print(f"Protein filter loaded: {len(keep_proteins):,} proteins allowed")

        # ── Load parquet ──────────────────────────────────────────────────────
        df = pd.read_parquet(
            Path(parquet_path).expanduser(),
            columns=[
                "entry_id", "protein", "sequence", "SMILES",
                "pIC50", "assay", "all_passed", "iptm",
            ],
        )

        # ── Filters ───────────────────────────────────────────────────────────
        if filter_all_passed:
            df = df[df["all_passed"] == True]

        if assay_filter is not None:
            df = df[df["assay"] == assay_filter]

        # Apply protein filter (drops at-risk and unannotated long proteins)
        before = len(df)
        df = df[df["protein"].isin(keep_proteins)]
        print(f"Protein filter: {before:,} -> {len(df):,} rows "
              f"(dropped {before-len(df):,})")

        if entry_ids is not None:
            df = df[df["entry_id"].isin(entry_ids)]

        # ── Deduplication ─────────────────────────────────────────────────────
        if deduplicate:
            df = (
                df.sort_values("iptm", ascending=False)
                .drop_duplicates(subset=["sequence", "SMILES"])
                .reset_index(drop=True)
            )

        df = df.dropna(subset=["pIC50", "SMILES", "sequence"]).reset_index(drop=True)

        self.df = df
        print(f"Dataset ready: {len(self.df):,} samples across "
              f"{self.df['protein'].nunique():,} proteins")

    def __len__(self) -> int:
        return len(self.df)

    def __getitem__(self, idx: int):
        row = self.df.iloc[idx]

        # ── Protein embedding (precomputed) ───────────────────────────────────
        emb_path = self.esm_cache_dir / f"{row['protein']}.pt"
        if not emb_path.exists():
            raise FileNotFoundError(
                f"ESM2 embedding not found: {emb_path}\n"
                f"Run scripts/precompute_esm.py first."
            )
        protein_emb = torch.load(emb_path, weights_only=True)  # [D]

        # ── Ligand graph ──────────────────────────────────────────────────────
        graph = smiles_to_graph(row["SMILES"])
        if graph is None:
            graph = Data(
                x=torch.zeros(1, 14),
                edge_index=torch.zeros(2, 1, dtype=torch.long),
                edge_attr=torch.zeros(1, 3),
            )

        # ── Label ─────────────────────────────────────────────────────────────
        pic50 = torch.tensor(float(row["pIC50"]), dtype=torch.float)

        return protein_emb, graph, pic50

    # ── Utility ───────────────────────────────────────────────────────────────

    def unique_proteins(self) -> list[str]:
        return self.df["protein"].unique().tolist()

    def pic50_stats(self) -> dict:
        s = self.df["pIC50"]
        return {
            "count": len(s),
            "mean": float(s.mean()),
            "std": float(s.std()),
            "min": float(s.min()),
            "max": float(s.max()),
        }
