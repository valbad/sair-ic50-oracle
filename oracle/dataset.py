"""
PyTorch Dataset for the SAIR IC50 oracle.

Each sample is a (residues, chem_emb, family_idx, pic50_norm, pic50_raw,
protein_name) tuple.

Per-protein pIC50 normalization:
    Targets are normalized per protein (zero mean, unit std) so the model learns
    within-protein ranking rather than between-protein scale differences.
    Normalization stats are computed from this split's own data.  Callers can
    pass `pic50_norms` explicitly to override (e.g., to use training stats for
    a downstream inference step).  All reported metrics are in raw pIC50 space —
    callers must denormalize model outputs before computing Spearman / MAE.

Protein family index:
    Mapped from the 'family' column in sair.parquet.  Unknown families fall back
    to the 'other' bucket (idx 5).  The mapping is intentionally stable:
    FAMILY_TO_IDX order must match the nn.Embedding size in oracle/model.py.
"""

from __future__ import annotations

from pathlib import Path
from typing import Optional

import pandas as pd
import torch
from torch.utils.data import Dataset

# ── Family mapping (must match n_families in config/default.yaml) ─────────────

FAMILIES     = ["kinase", "enzyme", "GPCR", "nuclear receptor", "phosphatase", "other"]
FAMILY_TO_IDX = {f: i for i, f in enumerate(FAMILIES)}
N_FAMILIES    = len(FAMILIES)   # 6


# ── Protein filter ─────────────────────────────────────────────────────────────

def load_keep_proteins(annotations_csv: str | Path) -> set[str]:
    """
    Load the set of proteins to keep from data/long_protein_annotations.csv.
    Short proteins always kept. Long proteins kept only if all annotated
    binding sites fall within the first 1022 residues (ESM2 limit).
    """
    df = pd.read_csv(Path(annotations_csv).expanduser())
    return set(df[df["keep"] == True]["protein"].tolist())


# ── Dataset ────────────────────────────────────────────────────────────────────

class SAIRDataset(Dataset):
    """
    Loads SAIR parquet, filters rows, and serves
    (residues, chem_emb, family_idx, pic50_norm, pic50_raw, protein_name) tuples.

    Args:
        parquet_path:       Path to sair.parquet.
        esm_cache_dir:      Directory with mean-pooled ESM2 .pt files
                            (protein.pt → Tensor[1280] float32).
        annotations_csv:    Path to long_protein_annotations.csv (keep-protein filter).
        entry_ids:          If provided, restrict to these entry IDs (train/val split).
        chem_emb_cache_path: Path to ChemBERTa cache
                            ({smiles: Tensor[768]}, generated by precompute_ligands.py
                            --chemberta-output).
        pic50_norms:        Pre-computed {protein: (mean, std)} dict.  If None,
                            per-protein stats are computed from this split.
        residue_cache_dir:  Per-residue ESM2 dir (legacy / future use).
        max_residues:       Truncation limit for per-residue tensors.
    """

    def __init__(
        self,
        parquet_path: str | Path,
        esm_cache_dir: str | Path,
        annotations_csv: str | Path = "data/long_protein_annotations.csv",
        entry_ids: Optional[list] = None,
        filter_all_passed: bool = True,
        assay_filter: Optional[str] = "biochem",
        deduplicate: bool = True,
        chem_emb_cache_path: Optional[str | Path] = None,
        normalize_targets: bool = True,
        pic50_norms: Optional[dict[str, tuple[float, float]]] = None,
        residue_cache_dir: Optional[str | Path] = None,
        max_residues: int = 1022,
        # legacy arg kept for backward compat — not used
        ligand_cache_path: Optional[str | Path] = None,
    ):
        residue_cache_dir = Path(residue_cache_dir).expanduser() if residue_cache_dir else None

        # ── Load keep-protein set ─────────────────────────────────────────────
        keep_proteins = load_keep_proteins(annotations_csv)
        print(f"Protein filter loaded: {len(keep_proteins):,} proteins allowed")

        # ── Load parquet ──────────────────────────────────────────────────────
        df = pd.read_parquet(
            Path(parquet_path).expanduser(),
            columns=[
                "entry_id", "protein", "sequence", "SMILES",
                "pIC50", "assay", "all_passed", "iptm", "family",
            ],
        )

        # ── Filters ───────────────────────────────────────────────────────────
        if filter_all_passed:
            df = df[df["all_passed"] == True]

        if assay_filter is not None:
            df = df[df["assay"] == assay_filter]

        before = len(df)
        df = df[df["protein"].isin(keep_proteins)]
        print(f"Protein filter: {before:,} -> {len(df):,} rows "
              f"(dropped {before - len(df):,})")

        if entry_ids is not None:
            typed_ids = pd.array(entry_ids, dtype=df["entry_id"].dtype)
            df = df[df["entry_id"].isin(typed_ids)]

        # ── Deduplication ─────────────────────────────────────────────────────
        if deduplicate:
            df = (
                df.sort_values("iptm", ascending=False)
                .drop_duplicates(subset=["sequence", "SMILES"])
                .reset_index(drop=True)
            )

        df = df.dropna(subset=["pIC50", "SMILES", "sequence"]).reset_index(drop=True)

        print(f"Dataset ready: {len(df):,} samples across "
              f"{df['protein'].nunique():,} proteins")

        # ── Family map ────────────────────────────────────────────────────────
        family_series = df.groupby("protein")["family"].first().fillna("other")
        self._family_map: dict[str, int] = {
            p: FAMILY_TO_IDX.get(str(fam), FAMILY_TO_IDX["other"])
            for p, fam in family_series.items()
        }

        self.df = df[["entry_id", "protein", "SMILES", "pIC50"]].copy()
        del df

        # ── Per-protein pIC50 normalization ───────────────────────────────────
        if not normalize_targets:
            # Identity transform — model predicts raw pIC50 directly
            proteins = self.df["protein"].unique().tolist()
            self.pic50_norms: dict[str, tuple[float, float]] = {
                p: (0.0, 1.0) for p in proteins
            }
            self.pic50_norms["__global__"] = (0.0, 1.0)
        elif pic50_norms is not None:
            self.pic50_norms = pic50_norms
        else:
            self.pic50_norms = {}
            for protein, group in self.df.groupby("protein")["pIC50"]:
                if len(group) < 2:
                    self.pic50_norms[protein] = (float(group.iloc[0]), 1.0)
                else:
                    m = float(group.mean())
                    s = float(group.std())
                    self.pic50_norms[protein] = (m, max(s, 1e-3))
            # global fallback for unseen proteins (GFlowNet inference)
            all_pic50 = self.df["pIC50"]
            self.pic50_norms["__global__"] = (
                float(all_pic50.mean()),
                max(float(all_pic50.std()), 1e-3),
            )

        # ── Pre-load ESM embeddings ───────────────────────────────────────────
        unique_proteins = self.df["protein"].unique().tolist()

        if residue_cache_dir is not None:
            missing = [p for p in unique_proteins
                       if not (residue_cache_dir / f"{p}.pt").exists()]
            if missing:
                raise FileNotFoundError(
                    f"{len(missing)} per-residue ESM embeddings missing in "
                    f"{residue_cache_dir}. Run: python scripts/precompute_esm.py "
                    f"--residue-dir {residue_cache_dir}"
                )
            print(f"Pre-loading {len(unique_proteins):,} per-residue ESM tensors "
                  f"(max {max_residues} residues) ... ", end="", flush=True)
            self._residue_cache: dict[str, torch.Tensor] = {
                p: torch.load(
                    residue_cache_dir / f"{p}.pt", weights_only=True
                )[:max_residues]
                for p in unique_proteins
            }
        else:
            esm_cache_dir_p = Path(esm_cache_dir).expanduser()
            missing = [p for p in unique_proteins
                       if not (esm_cache_dir_p / f"{p}.pt").exists()]
            if missing:
                raise FileNotFoundError(
                    f"{len(missing)} ESM embeddings missing in {esm_cache_dir_p}."
                )
            print(f"Pre-loading {len(unique_proteins):,} mean-pooled ESM embeddings "
                  f"... ", end="", flush=True)
            _mean_cache = {
                p: torch.load(esm_cache_dir_p / f"{p}.pt", weights_only=True)
                for p in unique_proteins
            }
            self._residue_cache = {
                p: emb.unsqueeze(0).half()   # [1, D] float16
                for p, emb in _mean_cache.items()
            }

        total_mb = sum(
            t.numel() * t.element_size() for t in self._residue_cache.values()
        ) / 1e6
        print(f"done ({total_mb:.0f} MB)")

        # ── Load ChemBERTa cache ──────────────────────────────────────────────
        unique_smiles = self.df["SMILES"].unique().tolist()

        if chem_emb_cache_path is not None:
            cache_path = Path(chem_emb_cache_path).expanduser()
            if not cache_path.exists():
                raise FileNotFoundError(
                    f"ChemBERTa cache not found at {cache_path}. "
                    f"Run: python scripts/precompute_ligands.py "
                    f"--parquet {parquet_path} --chemberta-output {cache_path}"
                )
            print(f"Loading ChemBERTa cache from {cache_path} ... ", end="", flush=True)
            full_cache: dict = torch.load(cache_path, weights_only=False)
            # Values may be numpy arrays (written by precompute_ligands.py) or
            # torch tensors (legacy).  Keep as-is; __getitem__ converts to tensor.
            self._chem_emb_cache: dict = {s: full_cache[s] for s in unique_smiles}
            print(f"done ({len(self._chem_emb_cache):,} entries)")
        else:
            # Compute in memory — slow (~5 min), requires transformers + GPU
            print(f"chem_emb_cache_path not provided — computing {len(unique_smiles):,} "
                  "ChemBERTa embeddings in memory (this is slow; consider precomputing).")
            import numpy as np
            from transformers import AutoTokenizer, AutoModel
            from tqdm.auto import tqdm
            _model_name = "seyonec/ChemBERTa-zinc-base-v1"
            _tokenizer  = AutoTokenizer.from_pretrained(_model_name)
            _model      = AutoModel.from_pretrained(_model_name).eval()
            # CPU only — MPS accumulates unified memory unboundedly across batches
            self._chem_emb_cache = {}
            BATCH = 128
            batches = [unique_smiles[i : i + BATCH] for i in range(0, len(unique_smiles), BATCH)]
            with torch.no_grad():
                for batch_smiles in tqdm(batches, desc="ChemBERTa (CPU)"):
                    enc = _tokenizer(
                        batch_smiles, return_tensors="pt",
                        max_length=128, truncation=True, padding=True,
                    )
                    out = _model(**enc)
                    cls = out.last_hidden_state[:, 0, :].float().numpy()
                    for smi, emb in zip(batch_smiles, cls):
                        self._chem_emb_cache[smi] = emb.copy()

    # ── Dataset interface ──────────────────────────────────────────────────────

    def __len__(self) -> int:
        return len(self.df)

    def __getitem__(self, idx: int):
        row       = self.df.iloc[idx]
        protein   = row["protein"]
        smiles    = row["SMILES"]
        pic50_raw = float(row["pIC50"])

        residues   = self._residue_cache[protein]           # [L, D] float16
        raw_emb    = self._chem_emb_cache[smiles]
        chem_emb   = (                                      # [768] float32
            torch.from_numpy(raw_emb).float()
            if not isinstance(raw_emb, torch.Tensor)
            else raw_emb.float()
        )
        family_idx = self._family_map[protein]              # int

        mean_, std_ = self.pic50_norms[protein]
        pic50_norm  = torch.tensor((pic50_raw - mean_) / std_, dtype=torch.float)
        pic50_raw_t = torch.tensor(pic50_raw, dtype=torch.float)

        return residues, chem_emb, family_idx, pic50_norm, pic50_raw_t, protein

    # ── Utility ───────────────────────────────────────────────────────────────

    def unique_proteins(self) -> list[str]:
        return self.df["protein"].unique().tolist()

    def pic50_stats(self) -> dict:
        s = self.df["pIC50"]
        return {
            "count": len(s),
            "mean":  float(s.mean()),
            "std":   float(s.std()),
            "min":   float(s.min()),
            "max":   float(s.max()),
        }
